package com.generics.generics.level1.exercise01;

public class NoGenericMethods {
    private final Object item1;
    private final Object item2;
    private final Object item3;

    public NoGenericMethods(Object item1, Object item2, Object item3) {
        this.item1 = item1;
        this.item2 = item2;
        this.item3 = item3;

    }

    public Object getItem1() {
        return item1;

    }

    public Object getItem2() {
        return item2;

    }

    public Object getItem3() {
        return item3;

    }
}
